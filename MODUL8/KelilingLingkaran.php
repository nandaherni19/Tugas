<?php
// Jari-jari lingkaran
$jariJari = 15;

// Konstanta phi (π)
$phi = 3.14;

// Rumus keliling lingkaran: K = 2 * π * r
$keliling = 2 * $phi * $jariJari;

// Tampilkan hasil
echo "<h3>Menghitung Keliling Lingkaran</h3>";
echo "Jari-jari: " . $jariJari . " cm<br>";
echo "Keliling: " . $keliling . " cm";
?>
